# 将数字类型转换成字符串
bum_str = str(123)
print(type(bum_str),bum_str)

float_str = str(12.3)
print(type(float_str),float_str)

# 将字符串转换成数字
num = int("11")
print(type(num),num)

num2 = float("12.3")
print(type(num2),num2)

# 整数转浮点数
float_num = float(12)
print(type(float_num),float_num)

# 浮点数转整数

int_num = int(12.7)
print(type(int_num),int_num)